#!/usr/bin/env python3
"""
This module contains a function to calculate the floor of a float.
"""


import math

def floor(n: float) -> int:
    """
    Calculate the floor of a float and return it as an integer.

    Args:
        n (float): The input float number.

    Returns:
        int: The floor of the input float as an integer.
    """
    return math.floor(n)
